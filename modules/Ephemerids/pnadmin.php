/**
 * View ephemerids
 * This is a standard function called to present the administrator with a list
 * of all items held by the module.
 * @author Mark West
 * @return string HTML string
 */
function Ephemerids_admin_view()
{
    // Security check - important to do this as early as possible to avoid
    // potential security holes or just too much wasted processing.  However,
    // in this case we had to wait until we could obtain the item name to
    // complete the instance information so this is the first chance we get to
    // do the check
    if (!pnSecAuthAction(0, 'Ephemerids::', '::', ACCESS_EDIT)) {
        return pnVarPrepHTMLDisplay(_MODULENOAUTH);
    }

    // Get parameters from whatever input we need.  All arguments to this
    // function should be obtained from pnVarCleanFromInput(), getting them
    // from other places such as the environment is not allowed, as that makes
    // assumptions that will not hold in future versions of PostNuke
    $startnum = pnVarCleanFromInput('startnum');

    // Create output object - this object will store all of our output so that
    // we can return it easily when required
	$pnRender =& new pnRender('Ephemerids');

	// As Admin output changes often, we do not want caching.
	$pnRender->caching = false;

    // The user API function is called.  This takes the number of items
    // required and the first number in the list of all items, which we
    // obtained from the input and gets us the information on the appropriate
    // items.
    $items = pnModAPIFunc('Ephemerids',
                          'user',
                          'getall',
                          array('startnum' => $startnum,
                                'numitems' => pnModGetVar('Ephemerids',
                                                          'itemsperpage')));

    $ephemerids = array();
    if(isset($items) && is_array($items)) {
        foreach ($items as $item) {

            $row = array();

            if (pnSecAuthAction(0, 'Ephemerids::', "$item[content]::$item[eid]", ACCESS_READ)) {
    
                // Options for the item.  Note that each item has the appropriate
                // levels of authentication checked to ensure that it is suitable
                // for display
                $options = array();
                if (pnSecAuthAction(0, 'Ephemerids::', "$item[content]::$item[eid]", ACCESS_EDIT)) {
                    $options[] = array('url' => pnModURL('Ephemerids', 'admin', 'modify', array('eid' => $item['eid'])),
                                       'title' => _EDIT);
                    if (pnSecAuthAction(0, 'Ephemerids::', "$item[content]::$item[eid]", ACCESS_DELETE)) {
                        $options[] = array('url' => pnModURL('Ephemerids', 'admin', 'delete', array('eid' => $item['eid'])),
                                           'title' => _DELETE);
                    }
                }
                $item['options'] = $options;
                $ephemerids[] = $item;
			}
        }
    }
    $pnRender->assign('ephemerids', $ephemerids);

    // Assign the values for the smarty plugin to produce a pager in case of there
    // being many items to display.
    //
    // Note that this function includes another user API function.  The
    // function returns a simple count of the total number of items in the item
    // table so that the pager function can do its job properly
	$pnRender->assign('pager', array('numitems' => pnModAPIFunc('Ephemerids', 'user', 'countitems'),
	                                 'itemsperpage' => pnModGetVar('Ephemerids', 'itemsperpage')));

    // Return the output that has been generated by this function
    return $pnRender->fetch('ephemerids_admin_view.htm');
}

